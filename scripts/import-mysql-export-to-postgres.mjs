const synthesizeMissingParents = (rowsByTable) => {
  const synthesizedCounts = new Map();

  const addRows = (tableName, rows) => {
    if (rows.length === 0) {
      return;
    }

    rowsByTable.get(tableName).push(...rows);
    synthesizedCounts.set(tableName, (synthesizedCounts.get(tableName) ?? 0) + rows.length);
  };

  const userRows = rowsByTable.get("User");
  const userIds = buildIdSet(userRows, "id");
  const missingUsers = new Map();

  for (const [tableName, field] of [
    ["Library", "userId"],
    ["Account", "userId"],
    ["Session", "userId"],
    ["Notify", "toUserId"],
    ["MangaComment", "userId"],
    ["ChapterComment", "userId"],
  ]) {
    for (const row of rowsByTable.get(tableName)) {
      const userId = row[field];

      if (userId && !userIds.has(userId) && !missingUsers.has(userId)) {
        missingUsers.set(userId, row);
      }
    }
  }

  addRows(
    "User",
    [...missingUsers.entries()].map(([userId, row]) => buildPlaceholderUser(userId, row)),
  );

  const mangaRows = rowsByTable.get("Manga");
  const mangaIds = buildIdSet(mangaRows, "mangadexId");
  const missingManga = new Map();

  for (const tableName of ["MangaComment", "LibraryManga"]) {
    for (const row of rowsByTable.get(tableName)) {
      const mangaId = row.mangaId;

      if (mangaId && !mangaIds.has(mangaId) && !missingManga.has(mangaId)) {
        missingManga.set(mangaId, row);
      }
    }
  }

  addRows(
    "Manga",
    [...missingManga.entries()].map(([mangaId, row]) => buildPlaceholderManga(mangaId, row)),
  );

  const chapterRows = rowsByTable.get("Chapter");
  const chapterIds = buildIdSet(chapterRows, "mangadexId");
  const missingChapter = new Map();

  for (const row of rowsByTable.get("ChapterComment")) {
    const chapterId = row.chapterId;

    if (chapterId && !chapterIds.has(chapterId) && !missingChapter.has(chapterId)) {
      missingChapter.set(chapterId, row);
    }
  }

  addRows(
    "Chapter",
    [...missingChapter.entries()].map(([chapterId, row]) =>
      buildPlaceholderChapter(chapterId, row),
    ),
  );

  return synthesizedCounts;
};

const findMissingReferences = (rowsByTable) => {
  const userIds = buildIdSet(rowsByTable.get("User"), "id");
  const mangaIds = buildIdSet(rowsByTable.get("Manga"), "mangadexId");
  const chapterIds = buildIdSet(rowsByTable.get("Chapter"), "mangadexId");
  const libraryIds = buildIdSet(rowsByTable.get("Library"), "id");
  const mangaCommentIds = buildIdSet(rowsByTable.get("MangaComment"), "id");
  const chapterCommentIds = buildIdSet(rowsByTable.get("ChapterComment"), "id");

  const checks = [
    ["Library", "userId", userIds],
    ["Account", "userId", userIds],
    ["Session", "userId", userIds],
    ["Notify", "toUserId", userIds],
    ["MangaComment", "userId", userIds],
    ["MangaComment", "mangaId", mangaIds],
    ["MangaComment", "parentId", mangaCommentIds],
    ["ChapterComment", "userId", userIds],
    ["ChapterComment", "chapterId", chapterIds],
    ["ChapterComment", "parentId", chapterCommentIds],
    ["LibraryManga", "libraryId", libraryIds],
    ["LibraryManga", "mangaId", mangaIds],
  ];

  return checks
    .map(([tableName, field, target]) => ({
      tableName,
      field,
      count: rowsByTable
        .get(tableName)
        .filter((row) => row[field] != null && !target.has(row[field])).length,
    }))
    .filter((entry) => entry.count > 0);
};

const main = async () => {
  const manifest = await readManifest();
  const rowsByTable = await readAllTableRows();
  const expectedCounts = new Map(
    manifest.tables.map((table) => [table.tableName, table.rowCount]),
  );
  const synthesizedCounts = synthesizeMissingParents(rowsByTable);

  for (const [tableName, count] of synthesizedCounts.entries()) {
    expectedCounts.set(tableName, (expectedCounts.get(tableName) ?? 0) + count);
  }

  const unresolvedReferences = findMissingReferences(rowsByTable);

  if (unresolvedReferences.length > 0) {
    throw new Error(
      `Unresolved references remain: ${JSON.stringify(unresolvedReferences)}`,
    );
  }

  const client = new Client({
    connectionString: targetDatabaseUrl,
    statement_timeout: 0,
  });

  await client.connect();

  try {
    console.log(`[source-export] ${sourceExportDir}`);
    console.log(`[target-db] ${targetDatabaseUrl.replace(/:[^:@/]+@/, ":***@")}`);

    for (const [tableName, count] of synthesizedCounts.entries()) {
      console.log(`[synthesize] ${tableName}: +${count} placeholder row(s)`);
    }

    await client.query("BEGIN");
    await client.query("SET LOCAL client_min_messages = WARNING");

    for (const { tableName } of dropOrder) {
      await client.query(`DROP TABLE IF EXISTS "${tableName}" CASCADE`);
    }

    await client.query(`DROP TYPE IF EXISTS "Category" CASCADE`);
    await client.query(enumSql);

    for (const statement of createSchemaStatements) {
      await client.query(statement);
    }

    await client.query("SET CONSTRAINTS ALL DEFERRED");

    for (const table of importOrder) {
      const rows = rowsByTable.get(table.tableName) ?? [];

      if (rows.length === 0) {
        console.log(`[import] ${table.tableName}: 0 row(s)`);
        continue;
      }

      for (const rowChunk of chunk(rows, batchSize)) {
        const insert = buildInsert(table.tableName, table.columns, rowChunk);
        await client.query(insert);
      }

      console.log(`[import] ${table.tableName}: ${rows.length} row(s)`);
    }

    await client.query(`
      SELECT setval(
        pg_get_serial_sequence('"Notify"', 'id'),
        COALESCE((SELECT MAX("id") FROM "Notify"), 1),
        EXISTS (SELECT 1 FROM "Notify")
      )
    `);

    for (const { tableName } of importOrder) {
      const result = await client.query(`SELECT COUNT(*)::int AS count FROM "${tableName}"`);
      const actualCount = result.rows[0].count;
      const expectedCount = expectedCounts.get(tableName) ?? 0;

      if (actualCount !== expectedCount) {
        throw new Error(
          `Count mismatch for ${tableName}: expected ${expectedCount}, got ${actualCount}`,
        );
      }

      console.log(`[verify] ${tableName}: ${actualCount} row(s)`);
    }

    await client.query("COMMIT");
    console.log("[done] Import completed successfully.");
  } catch (error) {
    await client.query("ROLLBACK").catch(() => {});
    throw error;
  } finally {
    await client.end().catch(() => {});
  }
};

main().catch((error) => {
  console.error("[import-failed]");
  console.error(error);
  process.exitCode = 1;
});

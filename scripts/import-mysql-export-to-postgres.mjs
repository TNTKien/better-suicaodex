const chunk = (items, size) => {
  const chunks = [];

  for (let index = 0; index < items.length; index += size) {
    chunks.push(items.slice(index, index + size));
  }

  return chunks;
};

const buildInsert = (tableName, columns, rows) => {
  const quotedColumns = columns.map(({ name }) => `"${name}"`).join(", ");
  const values = [];
  const placeholders = rows.map((row, rowIndex) => {
    const rowPlaceholders = columns.map((column, columnIndex) => {
      values.push(convertValue(row[column.name], column.type));
      return `$${rowIndex * columns.length + columnIndex + 1}`;
    });

    return `(${rowPlaceholders.join(", ")})`;
  });

  return {
    text: `INSERT INTO "${tableName}" (${quotedColumns}) VALUES ${placeholders.join(", ")}`,
    values,
  };
};

const buildIdSet = (rows, key) => new Set(rows.map((row) => row[key]));

const truncate = (value, maxLength) =>
  typeof value === "string" && value.length > maxLength ? value.slice(0, maxLength) : value;


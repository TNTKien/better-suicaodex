const readTableRows = async (tableName) => {
  const filePath = path.join(sourceExportDir, "data", `${tableName}.ndjson`);
  const raw = await fs.readFile(filePath, "utf8");

  if (raw.trim().length === 0) {
    return [];
  }

  return raw
    .trim()
    .split("\n")
    .filter((line) => line.length > 0)
    .map((line) => JSON.parse(line));
};

